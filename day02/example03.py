# 可以右键找到refactor rename给file中的所有变量重命名
"""
使用input函数从键盘输入数据
使用input函数将输入的内容处理成整数
"""
a1 = int(input('a= '))
b2 =int (input('b= '))
# 如果不写这个int( )编译器默认输入的是字符
print(a1 + b2)
print(a1 - b2)
print(a1 * b2)
print(a1 // b2)  # 整数除法
print(a1 / b2)  # 真正的除法
print(a1 % b2)  # 取模
print(a1 ** b2)  # 求幂 计算123的45次方
