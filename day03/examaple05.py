for i in range(1,11):     # 这个range（）里的数据是左闭右开
    print(i,'hello,world!')
    print('goodbye!')
print('byebye!')

for i in range(1,11,2):     # 这个range（）最后一个是步长
    print(i,'hello,world!')
    print('goodbye!')
print('byebye!')